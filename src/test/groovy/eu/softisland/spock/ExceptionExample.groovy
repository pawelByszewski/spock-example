package eu.softisland.spock

import spock.lang.Specification

class ExceptionExample extends Specification {


    def "catch some exception"() {
        given:
            ThrowingEx throwingEx = new ThrowingEx()

        when:
            throwingEx.tryMe()

        then:
            thrown(RuntimeException.class)
    }
}
















































































